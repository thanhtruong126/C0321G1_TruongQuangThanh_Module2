package bai1_introduction_to_java.thuc_hanh;

public class VariableDataType {
    public static void main(String[] args) {
        int i = 10;  // Khai báo biến i là kiểu int có giá trị là 10
        float f = 20.5f;  // Khai báo biến f là kiểu float có giá trị là 20.5
        double d = 20.5;  // Khai báo biến d là kiểu double có giá trị là 20.5
        boolean b = true;  // Khai báo biến b là kiểu boolean có giá trị là true
        char c = 'a';  // Khai báo biến c là kiểu char có giá trị kí tự là a
        String s = "Hà Nội";  // Khai báo biến s là kiểu String có giá trị chuỗi là Hà Nội

        System.out.println(i);
        System.out.println(f);
        System.out.println(d);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
    }
}
